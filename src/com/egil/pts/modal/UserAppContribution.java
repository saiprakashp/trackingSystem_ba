package com.egil.pts.modal;

public class UserAppContribution {

	private Long appId;
	private String appName;
	private Float appContribution;
	
	
	public Long getAppId() {
		return appId;
	}
	public void setAppId(Long appId) {
		this.appId = appId;
	}
	public String getAppName() {
		return appName;
	}
	public void setAppName(String appName) {
		this.appName = appName;
	}
	public Float getAppContribution() {
		return appContribution;
	}
	public void setAppContribution(Float appContribution) {
		this.appContribution = appContribution;
	}
	
	
}
